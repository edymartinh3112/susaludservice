package pe.com.susalud.beans;


public class AfiliadoRequestPayloadBean {

	private String codAfiliado;
	private String descAfiliado;
	private String dni;
	private String tipoAfiliacion;
	private String codMotivoAfiliacion;
	private String descMotivoAfiliacion;
	private String doc;

	public String getCodAfiliado() {
		return codAfiliado;
	}

	public void setCodAfiliado(String codAfiliado) {
		this.codAfiliado = codAfiliado;
	}

	public String getDescAfiliado() {
		return descAfiliado;
	}

	public void setDescAfiliado(String descAfiliado) {
		this.descAfiliado = descAfiliado;
	}

	public String getDni() {
		return dni;
	}

	public void setDni(String dni) {
		this.dni = dni;
	}

	public String getTipoAfiliacion() {
		return tipoAfiliacion;
	}

	public void setTipoAfiliacion(String tipoAfiliacion) {
		this.tipoAfiliacion = tipoAfiliacion;
	}

	public String getCodMotivoAfiliacion() {
		return codMotivoAfiliacion;
	}

	public void setCodMotivoAfiliacion(String codMotivoAfiliacion) {
		this.codMotivoAfiliacion = codMotivoAfiliacion;
	}

	public String getDescMotivoAfiliacion() {
		return descMotivoAfiliacion;
	}

	public void setDescMotivoAfiliacion(String descMotivoAfiliacion) {
		this.descMotivoAfiliacion = descMotivoAfiliacion;
	}

	public String getDoc() {
		return doc;
	}

	public void setDoc(String doc) {
		this.doc = doc;
	}

}
